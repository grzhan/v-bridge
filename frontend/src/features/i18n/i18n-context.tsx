const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, params?: TranslationParams) => string;
};

export const LANGUAGE_OPTIONS = [
  { code: 'zh-CN', labelKey: 'languages.zh-CN' },
  { code: 'en', labelKey: 'languages.en' },
] as const satisfies readonly { code: Language; labelKey: TranslationKey }[];

function getStoredLanguage(): Language {
  if (typeof window === 'undefined') {
    return DEFAULT_LANGUAGE;
  }
  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (stored === 'zh-CN' || stored === 'en') {
    return stored;
  }
  return DEFAULT_LANGUAGE;
}

function persistLanguage(language: Language) {
  if (typeof window === 'undefined') {
    return;
  }
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
}

function formatTemplate(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => {
    const value = params[token];
    return value === undefined || value === null ? '' : String(value);
  });
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => getStoredLanguage());

  useEffect(() => {
    persistLanguage(language);
  }, [language]);

  const handleChange = useCallback((next: Language) => {
    setLanguageState(next);
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    const translate = (key: TranslationKey, params?: TranslationParams) => {
      return formatTemplate(translations[language][key], params);
    };

    return {
      language,
      setLanguage: handleChange,
      t: translate,
    };
  }, [handleChange, language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}
